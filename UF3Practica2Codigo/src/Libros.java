public class Libros {

    private String nombre;

    private String tipo;

    private String editorial;

    private String autor;

    private int ano;

    public Libros(String nombre, String tipo, String editorial, String autor, int ano) {
    }

    public String getNombre() {
        return nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public String getEditorial() {
        return editorial;
    }

    public String getAutor() {
        return autor;
    }

    public int getAno() {
        return ano;
    }
}
